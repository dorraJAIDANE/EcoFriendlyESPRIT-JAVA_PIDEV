public class event {
    private int idevent;
     private float prix;

    public event(int idevent, float prix) {
        this.idevent = idevent;
        this.prix = prix;
    }

    public float getPrix() {
        return prix;
    }

    public void setPrix(float prix) {
        this.prix = prix;
    }

    public int getIdevent() {
        return idevent;
    }

    public void setIdevent(int idevent) {
        this.idevent = idevent;
    }
    
}
